public class deretbilangangenap {
        public static void main(String[] args) {
            System.out.println("Deret bilangan genap dari 2 hingga 20:");
    
            // Loop untuk mencetak deret bilangan genap dari 2 hingga 20
            for (int i = 2; i <= 20; i += 2) {
                System.out.print(i + " ");
            }
        }
    }
    
